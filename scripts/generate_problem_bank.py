# フォールバック分類
def classify_question(qnum: int, text: str) -> str:
    q = int(qnum)
    # 問番号で確定できるものを優先
    if q == 14:
        return "touki_14"
    if q == 13:
        return "kubun_13"
    if q == 46:
        return "juutaku_kinyuu"
    if q == 47:
        return "koukoku"
    for tid, _, tier, pat, _, _ in TOPICS:
        if tid in ("touki_14", "kubun_13", "juutaku_kinyuu", "koukoku"):
            continue
        if re.search(pat, text):
            return tid
    if q in (16, 17, 15) and re.search(r"都市計画|国土利用", text):
        return "toshi_keikaku" if "都市計画" in text else "kokudo_riyou"
    if q in (21, 22):
        return "nouchi"
    if 1 <= q <= 14:
        if re.search(r"いくつ", text):
            return "gyaku_soku"
        if re.search(r"相続|遺言|代襲", text):
            return "souzoku_saiban"
        if re.search(r"背信的|即時取得|第三者", text):
            return "kenri_ouhen"
        if re.search(r"抵当|競売|配当", text):
            return "teitou_haitou"
        return "minpo_general"
    if 15 <= q <= 22:
        return "kenchiku"
    if 23 <= q <= 32:
        return "baikai"
    if 33 <= q <= 38:
        return "inshi"
    if 39 <= q <= 45:
        return "juyou_jikou"
    return "gyaku_soku" if re.search(r"いくつ", text) else "minpo_general"


def main():
    data = json.loads(DATA.read_text(encoding="utf-8"))
    by_topic = defaultdict(list)
    topic_meta = {t[0]: t for t in TOPICS}

    for ex in EXAM_ORDER:
        if ex not in data:
            continue
        for qn, text in sorted(data[ex]["questions"].items(), key=lambda x: int(x[0])):
            tid = classify_question(qn, text)
            by_topic[tid].append({
                "exam": ex,
                "label": EXAM_LABEL[ex],
                "qnum": int(qn),
                "text": text.strip(),
            })

    lines = []
    lines.append("# 宅建 過去10年 問題バンク（3層構成）\n\n")
    lines.append("出典: [RETIO公式過去問](https://www.retio.or.jp/exam/past_ques_ans/other/)（平成28〜令和7）\n\n")
    lines.append("---\n\n")
    lines.append("## この問題集の使い方\n\n")
    lines.append("| 層 | 目標 | 目安問題数 | 勉強法 |\n")
    lines.append("|----|------|------------|--------|\n")
    lines.append("| **第I部 A** | ほぼ毎年出る論点を落とさない | 全問解く | 正解率90%まで反復 |\n")
    lines.append("| **第II部 B** | 合格点（33〜37点）を安定 | 代表問＋弱点のみ | 解説→類似問 |\n")
    lines.append("| **第III部 C** | 38点以上・満点狙い | 時間がある人 | 計算・判例・難問 |\n\n")
    lines.append("**近年の合格ライン（参考）**: 令和5〜7年度で**33〜37点/50問**（45問換算で約30〜33点）。\n")
    lines.append("→ **第I部を確実に**＋第II部の半分で合格圏。\n\n")
    lines.append("> OCR抽出のため誤字があります。原文は公式PDFで確認してください。\n\n")

    tier_names = {
        "A": ("第I部", "ほぼ出る問題（必須・合格の土台）", "毎年〜10/12回以上。ここを落とすと不合格リスク大。"),
        "B": ("第II部", "合格点のために対策すべき領域", "8〜11/12回。第I部の後に優先。"),
        "C": ("第III部", "満点・高得点のための領域", "頻度低・難易度高。余力で。"),
    }

    for tier_key in ("A", "B", "C"):
        part, title, desc = tier_names[tier_key]
        lines.append(f"---\n\n# {part}：{title}\n\n")
        lines.append(f"{desc}\n\n")

        for tid, name, tier, *_ in TOPICS:
            if tier != tier_key:
                continue
            meta = topic_meta[tid]
            qs = by_topic.get(tid, [])
            if not qs:
                continue

            lines.append(f"## {name}\n\n")
            lines.append(f"**出題数**: {len(qs)}問（12回分の抽出より）\n\n")
            lines.append("### 法律・ルールの説明\n\n")
            lines.append(meta[4] + "\n\n")
            lines.append("### 勉強のTips\n\n")
            lines.append(meta[5] + "\n\n")
            lines.append("### 過去問一覧\n\n")

            for i, q in enumerate(qs, 1):
                text = q["text"]
                if len(text) > 1200:
                    text = text[:1200] + "…（以下略・原文はPDF参照）"
                lines.append(f"#### 【{i}】{q['label']} 問{q['qnum']}\n\n")
                lines.append(f"{text}\n\n")
                lines.append("---\n\n")

    # 未分類・統計
    lines.append("---\n\n# 付録：論点別出題数\n\n")
    lines.append("| 論点 | 層 | 問題数 |\n|------|-----|--------:|\n")
    for tid, name, tier, *_ in TOPICS:
        c = len(by_topic.get(tid, []))
        if c:
            lines.append(f"| {name} | {tier} | {c} |\n")

    OUT.write_text("".join(lines), encoding="utf-8")
    total = sum(len(v) for v in by_topic.values())
    print(f"wrote {OUT}")
    print(f"total classified: {total}")
    for tier in "ABC":
        n = sum(len(by_topic[t[0]]) for t in TOPICS if t[2] == tier)
        print(f"  tier {tier}: {n} questions")


if __name__ == "__main__":
    main()
